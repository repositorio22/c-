//Multiplicacion de 2 numeros enteros//
#include<iostream>
using namespace std;
int main(){
	int a,b,multiplicacion;//datos necesarios
	//leer datos
	cout<<"ingresar primer numero: ";cin>>a;
	cout<<"ingresar segundo numero: ";cin>>b;
	//proceso de calculo de multiplicar
	multiplicacion=a*b;//a multiplicacion le asignamos el resultado de a *b
	//leer resultados
	cout<<"la multiplicacion de los dos numeros ingresados es: "<<multiplicacion<<endl;
	
	
	
return 0;
}
